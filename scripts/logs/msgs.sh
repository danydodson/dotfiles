#!/bin/bash

#######################################################################
# Log Helper Scripts                                                  #
#######################################################################

__info() { echo -e "\033[36m[INFO]\033[0m $1"; }
__user() { echo -e "\033[34m[OK]\033[0m $1"; }
__ok() { echo -e "\033[32m[OK]\033[0m $1"; }
__warning() { echo -e "\033[33m[WARNING]\033[0m $1"; }
__error() { echo -e "\033[31m[ERROR]\033[0m $1"; }
